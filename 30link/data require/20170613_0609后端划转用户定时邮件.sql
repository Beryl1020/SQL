  a.ia_name,
  a.group_id,
  sum(a1.六月九日划入资产) as 六月九日划入资产,
  sum(a2.当月净入金) as 当月净入金,
  sum(a3.当日交易额) as 当日交易额,
  sum(a3.当周交易额) as 当周交易额,
  sum(a3.当月交易额) as 当月交易额,
  round( sum(a3.当日交易额) / sum(a1.六月九日划入资产), 2) AS 当日资金利用率,
  round( sum(a3.当周交易额) / sum(a1.六月九日划入资产), 2) AS 当周资金利用率,
