FROM info_silver.dw_user_account a
  LEFT JOIN
  (SELECT
     a.ia_id,
     a.firm_id,
     sum(b.net_assets) AS 六月九日划入资产
   FROM
     info_silver.dw_user_account a
     JOIN silver_njs.tb_silver_data_center@silver_std b
       ON a.firm_id = b.firmid
   WHERE a.partner_id IN ('hht')
         AND b.hdate = '20170609'
         AND a.group_id IN (1, 7, 8, 111, 118)
   GROUP BY a.ia_id, a.firm_id) a1
    ON a.firm_id = a1.firm_id
  LEFT JOIN
  (SELECT
     a.ia_id,
     a.firm_id,
     sum(CASE WHEN b.inorout = 'A'
       THEN b.inoutmoney
         ELSE b.inoutmoney * (-1) END) AS 当月净入金
   FROM
     info_silver.dw_user_account a
     JOIN silver_njs.history_transfer@silver_std b
       ON a.firm_id = b.firmid
   WHERE a.partner_id IN ('hht')
         AND substr(b.fdate, 1, 6) = to_char(sysdate - 1, 'yyyymm')
         AND b.fdate <= to_char(sysdate - 1, 'yyyymmdd')
         AND a.group_id IN (1, 7, 8, 111, 118)
   GROUP BY a.ia_id, a.firm_id) a2
    ON a.firm_id = a2.firm_id
  LEFT JOIN
  (SELECT
     a.ia_id,
     a.firm_id,
     sum(b.contqty)        AS 当月交易额,
     sum(CASE WHEN b.fdate = to_char(sysdate - 1, 'yyyymmdd')
       THEN b.contqty END) AS 当日交易额,
     sum(CASE WHEN b.fdate - to_char(trunc(sysdate - 1, 'd'), 'yyyymmdd') <= 6
                   AND b.fdate - to_char(trunc(sysdate - 1, 'd'), 'yyyymmdd') >= 0
       THEN b.contqty END) AS 当周交易额
   FROM
     info_silver.dw_user_account a
     JOIN info_silver.ods_history_deal b
       ON a.firm_id = b.firmid
   WHERE a.partner_id IN ('hht')
         AND substr(b.fdate, 1, 6) = to_char(sysdate - 1, 'yyyymm')
         AND b.fdate <= to_char(sysdate - 1, 'yyyymmdd')
         AND a.group_id IN (1, 7, 8, 111, 118)
   GROUP BY a.ia_id, a.firm_id) a3
    ON a.firm_id = a3.firm_id
